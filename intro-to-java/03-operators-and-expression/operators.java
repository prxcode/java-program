

// Print stars in a pattern

public class operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 6 + a;
        int c = 6 % a;
        int d = 5;
        d += 5;
        System.out.println(b); // b= 6 + 4 = 10
        System.out.println(c); // c= 6 % 4 = 2
        System.out.println(d); 
        System.out.println(a>d);
        System.out.println(a>d && b>a); // false AND true = false
        System.out.println(a>d || b>a); // true OR true = true
        System.out.println(2&3);
    }
}

